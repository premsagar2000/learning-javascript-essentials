//Increament and Decrement Operators

//Postfix
console.log("postfix"); 
var number = 0;
console.log(number++); //Return the value before incrementing
console.log(number);
console.log(number--); //Return the value before decrementing
console.log(number);

console.log();

// Prefix
console.log("prefix");
var numberTwo = 0;
console.log(++numberTwo); //Return the value after incrementing
console.log(numberTwo); 
console.log(--numberTwo); //Return the value after decrementing
console.log(numberTwo); 

console.log();
console.log("for loop");
for(var i=0;i<=10;i++){
    console.log(i); 
}
